/*
Fazer a tabela  dos operadores && (AND), || (OR), ! (NOT), ^ (XOR)
*/

//Tabela vardade

  true  && true;  // => True
  true  && false; // => False
  false && true; // => False
  false && false;  // => False

  true  || true;  // => True
  true  || false; // => True
  false || true;  // => True
  false || false; // => False

  true  != true;  // => False
  true  != false; // => True
  false != true;  // => True
  false != false; // => False
    
  true  ^ true;  // => 0
  true  ^ false; // => 1
  false ^ true;  // => 1
  false ^ false; // => 0
